______________________________________________________________________________________<!--l. 77--><p class="noindent" ></span>
</p><!--l. 85--><p class="noindent" >Krukovsky was the first woman to gain a degree for her contributions to mathematics; she is also
known for her literary works and as a women’s rights activist. She was called Sofia Vasilievna
Krukovsky from birth; she is often called Sophie, being the anglicised version of Sofia, the second
being a familiar; and she has been called Sonya version by which she was known by her friends and
Sonja. Kovalevskaya is the female version of her husband’s name Kovalevsky which is often
transliterated as Kovalevskaia and infrequently as Kovalevskaja also, also known as Sonya
Kovalevsky, a masculine version of her surname.
</p><!--l. 88--><p class="noindent" >As a child, Sofja Kolwalewskaja’s a wall bedroom was wallpapered with her Father’s mathematics
notes from his student days being lectured by Ostrogradsky; the wall was wallpapered with it because
they ordered one roll short; the paper intrigued her as a child. Born of minor nobility as
a child, she was fortunate enough to be tutored in elementary maths and went on to get
tutoring in calculus from Strannoliubskii, a well—known advocate of higher education for
women.
</p><!--l. 91--><p class="noindent" >Her sister, who was older than her, met the son of the local priest when she was 16; he told her about
his university studies inspiring Anyuta to go to St Petersburg to live and proposed living in a commune
with young people living together without servants. Her Father did not allow this, and she reacted by
secretly writing and getting two pieces published under a male pseudonym. As a woman, Sofja could
not complete her education in Russia, and in 1868, she contracted a ”fictitious marriage” with
Vladimir Kovalevskij, so they could go to Vienna, where she studied physics while he studied
palaeontology.
</p><!--l. 93--><p class="noindent" >They moved to St Petersburg for several months, studying various subjects, and in 1869 they went to
the University of Heidelberg. There she attended courses in physics under such teachers as Hermann
von Helmholtz, Gustav Kirchhoff and Robert Bunsen and studied mathematics with Leo
Königsberger and Professor Paul du Bois-Reymond, who were students of Weierstrass. Paul du
Bois-Reymond is recognised as one of the first to this is the importance of the Cauchy convergence
criterion in his 1882 work.
</p><!--l. 95--><p class="noindent" >In 1869 at nineteen, on a trip to London for Vladimir to see his acquaintances, Charles Darwin and
Thomas Huxley, she was invited to attend George Eliot’s Sunday salons, where she met
Herbert Spencer, which led to a debate, at Eliot’s instigation, on ”woman’s capacity for
abstract thought”. The debate may have inspired the passage, ”In short, a woman was a
                                                                                      
                                                                                      
problem which, since Mr Brooke’s mind felt blank before it, could hardly be less complicated
than the revolutions of an irregular solid.” as Eliot was writing Middlemarch during that
period.
</p><!--l. 97--><p class="noindent" >In October 1870, Kovalevskaya moved to Berlin, where she began private lessons with Karl
Weierstrass, his willingness to do so may have been due to her grandfather, who immigrated from
Germany to Russia as the astronomer and geographer who he cited in one of his papers. Weierstrass
was often cited as the ”father of modern analysis”; he was a mathematical heavyweight; regardless of
leaving university without a degree, because his studies were to be in the fields of law,
economics, and finance, he was immediately in conflict with his hopes to study mathematics, he
went on to train as a teacher in Münster, during this period of study, Weierstrass attended
the lectures of Christoph Gudermann. Weierstrass went on to teach mathematics, botany,
physics and gymnastics. At the age of thirty-five, while suffering from a long illness, he
produced mathematical publications gaining him an honorary doctorate from the University of
Königsberg. Weierstrass’s contributions include formalising the continuity of a function and
complex analysis, proving the intermediate value theorem and the Bolzano–Weierstrass
theorem.
</p><!--l. 99--><p class="noindent" >Kowalewskaya met Weierstrass n 1870; he tutored her privately after failing to secure her admission to
the university. They had a fruitful intellectual and kindly personal relationship that ”transcended the
usual teacher-student relationship”. They shared letters from 1872 to 1874; many at the beginning
were about identifies of theta function, and many letters were written during holidays; therefore, they
did not discuss work. She took time out of her studies to assist her sister during the Paris
Commune.
</p><!--l. 101--><p class="noindent" >Over eighteen months, Sofja wrote three dissertations under Weierstrass’s direction in 1874; she
presented three papers—on partial differential equations, the dynamics of Saturn’s rings, and elliptic
integrals. The paper on partial differential equations contains what is now known as the
Cauchy–Kovalevskaya theorem, which proves the existence and analyticity of local solutions
uniqueness theorem for analytic partial differential equations associated with Cauchy initial value
problems. The paper on the reduction of abelian integrals to simpler elliptic integrals is of less
importance; it consisted of a skilled series of manipulations which showed her complete command of
Weierstrass’s theory.
</p><!--l. 103--><p class="noindent" >Sofia and Vladimir returned to Russia and, in 1878, gave birth to their daughter, nicknamed ’Foufie’.
Sofia could not gain a Professorship due to her sex, and Vladimir failed to acquire one due to his
radical beliefs; they tried to make money through schemes. After raising her daughter, Kovalevskaya
put Fufa under the care of relatives and friends at the age of two, resumed her work in mathematics,
and left Vladimir. In 1883, Vladimir committed suicide because he feared being prosecuted for his
role in a stock swindle.
</p><!--l. 105--><p class="noindent" >In 1883 she accepted an invitation for a teaching position at the University of Stockholm. Sofia was
offered a professor position at the University of Stockholm. Thanks to Mittag-Leffler took great
trouble and procured Sofia Kovalevskaya a position as full professor of mathematics at Stockholm
University. In 1888 Sofia was appointed the editor of the mathematics journal Acta Mathematica,
making her the first woman to hold the title. Sofia won the Prix Bordin award at a French Academy of
Science competition.
</p><!--l. 107--><p class="noindent" >Sofia established a close friendship with Mittag-Leffler’s sister, Anne Charlotte Edgren-Leffler, an
actress, novelist, and playwright. They wrote many stage plays, including the memoir- A Russian
Childhood. In 1890 she wrote Nihilist Girl, a semi-autobiographical account of her life. In June 1889,
she became the first woman to hold a chair at a European university, since the physicist Laura Bassi
                                                                                      
                                                                                      
(1711 – 1778) was the first woman ever to be granted a professorship at a university in 1750; once, she
was the highest-paid employee of the university. Maria Gaetana Agnesi (1718 – 1799) was the second
woman ever to be granted a professorship at a university; she is credited with writing the first book
discussing both differential and integral calculus and was a member of the faculty at the University of
Bologna,. At the early age of forty-one, she died when she caught pneumonia while returning from a
vacation.
